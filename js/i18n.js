/* =========================================================================
   Chezmaxo — EN / FR language toggle
   I18N (the translation dictionary) is generated at build time and loaded
   just before this file. This file only contains the switching logic.
   ========================================================================= */

(function () {
  var STORAGE_KEY = "site-lang";

  function getInitialLang() {
    try {
      var stored = window.localStorage.getItem(STORAGE_KEY);
      if (stored === "en" || stored === "fr") return stored;
    } catch (e) { /* localStorage unavailable — fall back to default */ }
    return "en";
  }

  function applyLang(lang) {
    if (typeof I18N === "undefined") return;

    document.documentElement.setAttribute("lang", lang);

    // Plain text content (headings, paragraphs, buttons, options, etc.)
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      var entry = I18N[key];
      if (entry && entry[lang] !== undefined) {
        el.textContent = entry[lang];
      }
    });

    // Attribute-based content: placeholders
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-placeholder");
      var entry = I18N[key];
      if (entry && entry[lang] !== undefined) {
        el.setAttribute("placeholder", entry[lang]);
      }
    });

    // Attribute-based content: meta tag "content" values
    document.querySelectorAll("[data-i18n-content]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-content");
      var entry = I18N[key];
      if (entry && entry[lang] !== undefined) {
        el.setAttribute("content", entry[lang]);
      }
    });

    // Toggle button state
    document.querySelectorAll("[data-lang-btn]").forEach(function (btn) {
      var isActive = btn.getAttribute("data-lang-btn") === lang;
      btn.classList.toggle("active", isActive);
      btn.setAttribute("aria-pressed", isActive ? "true" : "false");
    });

    try { window.localStorage.setItem(STORAGE_KEY, lang); } catch (e) { /* ignore */ }
    window.__currentLang = lang;
  }

  function initLangToggle() {
    var lang = getInitialLang();
    applyLang(lang);

    document.querySelectorAll("[data-lang-btn]").forEach(function (btn) {
      btn.addEventListener("click", function () {
        applyLang(btn.getAttribute("data-lang-btn"));
      });
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", initLangToggle);
  } else {
    initLangToggle();
  }
})();
